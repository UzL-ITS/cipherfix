void cf_init_target(void)
{
    // Ensure that there is no external randomness
    RAND_set_rand_method(&rand_meth);
}

void cf_run_target(bool dumpResult)
{
	classify(privateKey_d, sizeof(privateKey_d));
    classify(privateKey_p, sizeof(privateKey_p));
    classify(privateKey_q, sizeof(privateKey_q));
    classify(privateKey_dmp1, sizeof(privateKey_dmp1));
    classify(privateKey_dmq1, sizeof(privateKey_dmq1));
    classify(privateKey_iqmp, sizeof(privateKey_iqmp));

    // Load private key
    RSA *rsaPrivate = RSA_new();
    if(!rsaPrivate)
        ERR_print_errors_fp(stderr);

    rsaPrivate->n = BN_bin2bn(privateKey_n, sizeof(privateKey_n), NULL);
    rsaPrivate->e = BN_bin2bn(privateKey_e, sizeof(privateKey_e), NULL);
    rsaPrivate->d = BN_bin2bn(privateKey_d, sizeof(privateKey_d), NULL);
    rsaPrivate->p = BN_bin2bn(privateKey_p, sizeof(privateKey_p), NULL);
    rsaPrivate->q = BN_bin2bn(privateKey_q, sizeof(privateKey_q), NULL);
    rsaPrivate->dmp1 = BN_bin2bn(privateKey_dmp1, sizeof(privateKey_dmp1), NULL);
    rsaPrivate->dmq1 = BN_bin2bn(privateKey_dmq1, sizeof(privateKey_dmq1), NULL);
    rsaPrivate->iqmp = BN_bin2bn(privateKey_iqmp, sizeof(privateKey_iqmp), NULL);

    int signatureLength = sizeof(privateKey_n);
    uint8_t *signature = malloc(signatureLength);
    
    if(!RSA_sign(NID_sha256, m, sizeof(m), signature, &signatureLength, rsaPrivate))
        ERR_print_errors_fp(stderr);
    
    if(dumpResult)
        dump_secret(signature, signatureLength, "sig");

    RSA_free(rsaPrivate);
    free(signature);
}

void cf_prepare_next(void)
{
    // Increment message
    for(int i = 0; i < sizeof(m); ++i)
    {
        unsigned char tmp = m[i] + 1;
        m[i] = tmp;
        if(tmp != 0)
            break;
    }
}