void cf_init_target(void)
{
    // Initialize RNG and run a few rounds
    uint8_t *rngDummy = malloc(1024);
    myrng(NULL, rngDummy, 1024);
    free(rngDummy);
}

void cf_run_target(bool dumpResult)
{
    classify(key, sizeof(key));

    RsaKey k;
    int offset = 0;
    wc_InitRsaKey(&k, NULL);
    wc_RsaPrivateKeyDecode(key, &offset, &k, sizeof(key));

    WC_RNG rng;
    wc_InitRng(&rng);
    wc_RsaSetRNG(&k, &rng);

    unsigned char *sig = malloc(bits / 8);
    int size = wc_RsaSSL_Sign(m, sizeof(m), sig, bits / 8, &k, &rng);

    if(dumpResult)
        dump_secret(sig, size, "signature");

    free(sig);
}

void cf_prepare_next(void)
{
    // Increment message
    for(int i = 0; i < sizeof(m); ++i)
    {
        unsigned char tmp = m[i] + 1;
        m[i] = tmp;
        if(tmp != 0)
            break;
    }
}
